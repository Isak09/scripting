#! bin/bash
#########################
# Este script nos mostrara un archivo en pantalla
#########################
# El comando "more" sirve para mostrar archivos haciendo
# pausa en cada pantalla. Simplemente escribiendo el comando "more"
# y despues que archivo queremos ver.

more ejemplo.txt
# Ahora si quieres buscar algo en el archivo usas grep
grep "cualquier cosa" ejemplo.txt
